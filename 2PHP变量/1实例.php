<?php
    $x=5;
    $y=6;
    $z=$x+$y;
    echo $z;

    /*
    PHP变量规则
        变量是以$符号开头，其后是变量的名称
        变量名称必须以字母或下划线开头
        变量名不能以数字开头
        变量名称只能包含字母数字字符和下划线
        变量名称对大小写敏感
    */

    /*
    创建PHP变量
        PHP没有创建变量的命令。
        变量会在首次为其赋值时被创建
    */

    /*
    PHP是一门类型松散的语言
    PHP根据它的值，自动把变量转换为正确的数据类型。
    */
?>