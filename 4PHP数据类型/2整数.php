<?php
    /*
    整数规则：
        整数必须有至少一个数字（0-9）
        整数不能包含逗号或空格
        整数不能有小数点
        整数正负均可。
        可以用三种格式规定整数：十进制、十六进制（前缀是0x）或八进制（前缀是0）
    */

    //var_dump()会返回变量的数据类型和值

    $x=5985;
    var_dump($x);
    echo "<br>";

    $x=-345;
    var_dump($x);
    echo "<br>";

    $x=0x8c;
    var_dump($x);
    echo "<br>";

    $x=047;
    var_dump($x);
    echo "<br>";
    
?>